-- VIEWS
-- ============================================================

-- eigentuemer: customers who own at least one facility
create or replace view public.eigentuemer as
select distinct k.*
from public.kunden k
join public.anlagen a on a.kunden_id = k.id;

-- anlagen_details: flat view combining facility with owner and contact info
create or replace view public.anlagen_details as
select
  a.*,

  -- Owner data
  k.kundennr                        as owner_kundennr,
  coalesce(k.firma,
    concat_ws(' ', k.vorname, k.nachname))
                                    as owner_name,
  k.telefonnr                       as owner_telefonnr,
  k.email                           as owner_email,

  -- Resolved contact: either a Kunde or a standalone Kontakt
  case
    when a.kontakt_kunde_id is not null then 'kunde'
    when a.kontakt_id        is not null then 'kontakt'
    else null
  end                               as kontakt_typ,

  coalesce(
    concat_ws(' ', kk.vorname, kk.nachname),
    concat_ws(' ', kont.vorname, kont.nachname)
  )                                 as kontakt_name,

  coalesce(kk.telefonnr, kont.telefonnr)  as kontakt_telefonnr,
  coalesce(kk.email,     kont.email)      as kontakt_email

from public.anlagen a
join public.kunden k
  on k.id = a.kunden_id
left join public.kunden kk
  on kk.id = a.kontakt_kunde_id
left join public.kontakte kont
  on kont.id = a.kontakt_id;

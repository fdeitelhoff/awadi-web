-- ============================================================
-- AWADI Database Schema for Supabase (PostgreSQL)
-- Created: 2026-02-24
--
-- Source tables:
--   tbldef_anl_typen  → anl_typen
--   tblkunden
--   tblanl_eigentuemer  → kunden   (merged; EigentuemerNr = KundenNr)
--   tblanl              → anlagen
--   tblanl_bio          → anl_biologien
--   (derived)           → kontakte, wartungsvertraege
